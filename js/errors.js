/* =============================================================
   errors.js — แปลข้อความ error ของ Python ให้เป็นภาษาไทยที่เข้าใจง่าย
   หลักการ: error คือข้อมูล ไม่ใช่การลงโทษ -> โทนเป็นมิตร ชี้ทางต่อ
   ไม่ใช้ emoji (UI จะใส่ไอคอนเตือนแบบ SVG ให้เอง)
   ไม่บอกคำตอบตรง ๆ แค่สะกิดให้ไปดูจุดที่น่าจะพลาด
   ============================================================= */
// codeLine (ถ้าส่งมา) = ข้อความของบรรทัดที่ผิด ใช้ช่วยเดาอาการให้แม่นขึ้น
function translateError(raw, codeLine){
  const lower = raw.toLowerCase();
  const type  = raw.split(":")[0].trim();

  // อาการยอดฮิตของมือใหม่: ลืมใส่ : ท้ายบล็อก (if/elif/else/for/while/def...)
  // เช็คจากตัวบรรทัดจริงก่อน จะแม่นกว่าการเดาจากข้อความ error ดิบ
  if (codeLine && /^\s*(if|elif|else|for|while|def|try|except|finally|with|class)\b/.test(codeLine)
      && !codeLine.trim().endsWith(":")){
    return "ลืมใส่ : ท้ายบรรทัดไหม? บรรทัดที่ขึ้นต้นด้วย if / elif / else / for / while / def ต้องจบด้วยเครื่องหมาย :";
  }

  // SyntaxError มีหลายกรณีย่อย แยกข้อความให้ตรงอาการ
  if (type === "SyntaxError"){
    if (lower.includes("eof") || lower.includes("multi-line"))
      return "ดูเหมือนเปิดวงเล็บหรือเครื่องหมายไว้แต่ยังปิดไม่ครบนะ ลองนับ ( ) หรือ \" \" ให้เท่ากัน";
    if (lower.includes("bad token") || lower.includes("bad input"))
      return "รูปแบบบรรทัดนี้ยังไม่ถูกหลักภาษา ลองเช็คว่าลืมใส่ : ท้ายบรรทัด (if / for / def) หรือพิมพ์อะไรเกินมาไหม";
    return "รูปแบบบรรทัดนี้ยังไม่ถูกหลักภาษา ลองเทียบกับตัวอย่างด้านบน (ลืม : ท้าย if / for / def ไหม?)";
  }

  // ตาราง error ที่เหลือ -> ข้อความไทย
  const table = {
    "IndentationError":"บรรทัดนี้เว้นช่องหน้าไม่ตรงกับเพื่อน ลองจัดย่อหน้าให้ตรง (4 ช่อง)",
    "TabError":"บรรทัดนี้ปน Tab กับ Space ลองลบย่อหน้าแล้วเคาะ Space 4 ครั้งใหม่",
    "NameError":"มีชื่อตัวแปรหรือฟังก์ชันที่ยังไม่ถูกสร้าง หรือสะกดผิด ลองเช็คตัวสะกดอีกที",
    "TypeError":"เอาข้อมูลคนละชนิดมาทำด้วยกันไม่ได้ (เช่น ตัวเลข + ข้อความ) ต้องแปลงชนิดก่อนด้วย str() หรือ int()",
    "ValueError":"ค่าที่ใส่ไม่ตรงกับที่ต้องการ (เช่น เอาตัวอักษรไปแปลงเป็นเลขด้วย int())",
    "IndexError":"เรียกตำแหน่งใน list เกินที่มี จำไว้ว่าเริ่มนับจาก 0 นะ",
    "KeyError":"เรียก key ที่ไม่มีใน dictionary ลองเช็คชื่อ key",
    "AttributeError":"เรียกคำสั่งที่ชนิดข้อมูลนี้ไม่มี ลองเช็คว่าใช้ถูกชนิดไหม",
    "ZeroDivisionError":"หารด้วย 0 ไม่ได้นะ ลองเช็คตัวหาร",
    "ImportError":"โมดูลนี้ยังใช้ไม่ได้ในบทเรียนนี้ ใช้คำสั่งพื้นฐานก่อนนะ",
    "ModuleNotFoundError":"ไม่พบโมดูลที่เรียก บทเรียนนี้ยังไม่ต้อง import",
    "RecursionError":"ฟังก์ชันเรียกตัวเองวนไม่หยุด ลองใส่เงื่อนไขให้หยุดได้",
    "TimeLimitError":"โค้ดทำงานนานเกินไป อาจเป็นลูปที่ไม่มีวันหยุด ลองเช็คเงื่อนไข while",
    "OverflowError":"ตัวเลขใหญ่เกินจะคำนวณไหว ลองลดขนาดลง",
    "AssertionError":"เงื่อนไขใน assert เป็นเท็จ ลองเช็คค่าที่ได้",
    "UnboundLocalError":"ใช้ตัวแปรในฟังก์ชันก่อนกำหนดค่า ลองกำหนดค่าก่อนใช้",
    "KeyboardInterrupt":"การทำงานถูกหยุดกลางคัน ลองรันใหม่อีกครั้ง"
  };

  if (table[type]) return table[type];

  // เผื่อ error แปลก ๆ ที่ไม่อยู่ในตาราง -> ข้อความปลอบใจกลาง ๆ พร้อมชี้ทางต่อ
  return "มีบางอย่างยังไม่ถูก ลองอ่านโค้ดอีกรอบทีละบรรทัดช้า ๆ นะ ถ้าติดจริง ๆ กดปุ่ม \"ขอคำใบ้\" ได้เลย";
}
