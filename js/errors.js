/* =============================================================
   errors.js — แปลข้อความ error ของ Python ให้เป็นภาษาไทยที่เข้าใจง่าย
   หลักการ: error คือข้อมูล ไม่ใช่การลงโทษ -> โทนเป็นมิตร ชี้ทางต่อ
   ไม่ใช้ emoji (UI จะใส่ไอคอนเตือนแบบ SVG ให้เอง)
   ไม่บอกคำตอบตรง ๆ แค่สะกิดให้ไปดูจุดที่น่าจะพลาด
   ============================================================= */
// codeLine (ถ้าส่งมา) = ข้อความของบรรทัดที่ผิด ใช้ช่วยเดาอาการให้แม่นขึ้น
function translateError(raw, codeLine){
  const lower = raw.toLowerCase();
  const type  = raw.split(":")[0].trim();
  const line  = codeLine || "";

  // อาการยอดฮิตของมือใหม่: ลืมใส่ : ท้ายบล็อก (if/elif/else/for/while/def...)
  // เช็คจากตัวบรรทัดจริงก่อน จะแม่นกว่าการเดาจากข้อความ error ดิบ
  if (line && /^\s*(if|elif|else|for|while|def|try|except|finally|with|class)\b/.test(line)
      && !line.trim().endsWith(":")){
    return "ลืมใส่ : ท้ายบรรทัดไหม? บรรทัดที่ขึ้นต้นด้วย if / elif / else / for / while / def ต้องจบด้วยเครื่องหมาย :";
  }

  // อาการพิมพ์ผิดที่ "ดูจาก codeLine" รู้ได้เลย — เช็คก่อนแยกตามชนิด error
  // เพราะบางอาการ Skulpt มองเป็น TypeError ไม่ใช่ SyntaxError (เช่น print[...])
  // จึงต้องเช็คจากตัวบรรทัดก่อน ไม่งั้นจะตกไปตอบ "ผิดชนิดข้อมูล" แบบงง ๆ

  // (โค้งก่อน) อัญประกาศโค้ง (smart quote) จากการ copy ข้อความ หรือพิมพ์บนมือถือ
  // เช่น print(“Rin”) หรือ print(‘Rin’) — ตาเปล่าดูเหมือนถูก แต่ Python ไม่รับ
  if (line && /[“”‘’]/.test(line)){
    return "เครื่องหมายคำพูดเป็นแบบโค้ง (“ ”) ลองเปลี่ยนเป็นแบบตรง (\" \") โดยพิมพ์จากแป้นพิมพ์โดยตรงนะ";
  }

  // (วงเล็บผิดชนิด) ใช้ [ ] หรือ { } แทน ( ) ในการเรียกฟังก์ชัน เช่น print["Rin"]
  // อันนี้ Skulpt อาจมองเป็น TypeError จึงเช็คจากตัวบรรทัดก่อนเข้าตาราง error
  if (line && /\bprint\s*[\[\{]/.test(line)){
    return "การเรียกใช้ print ต้องใช้วงเล็บ ( ) ครอบนะ ไม่ใช่ [ ] หรือ { }";
  }

  // ── อาการเรื่องการตั้งชื่อตัวแปร / การกำหนดค่า (พบบ่อยใน Mission 2) ──
  // ตรวจจาก codeLine ก่อน เพราะแม่นกว่าการเดาจาก error ดิบ
  // สำคัญ: ต้องเป็น "=" ที่กำหนดค่าจริง ๆ เท่านั้น ไม่ใช่ == >= <= != += ฯลฯ
  // และไม่ใช่ "=" ที่อยู่ในสตริง (เช่น print("a = b")) ไม่งั้นจะแปลผิดในด่านอื่น
  if (line){
    // กลบเนื้อหาในเครื่องหมายคำพูดก่อน (เก็บความยาวไว้เท่าเดิมเพื่อให้ตำแหน่งตรง)
    const masked = line.replace(/"[^"]*"/g, function(m){ return '"' + " ".repeat(m.length - 2) + '"'; })
                       .replace(/'[^']*'/g, function(m){ return "'" + " ".repeat(m.length - 2) + "'"; });
    // หาตำแหน่ง "=" ตัวแรกที่เป็นการกำหนดค่า (ไม่ใช่ ==, >=, <=, !=, +=, -= ...)
    let eqAt = -1;
    for (let i = 0; i < masked.length; i++){
      if (masked[i] !== "=") continue;
      if (masked[i + 1] === "=") { i++; continue; }                  // == -> ไม่ใช่การกำหนดค่า
      if ("=<>!+-*/%&|^".indexOf(masked[i - 1]) !== -1) continue;    // >= <= != += ... -> ข้าม
      eqAt = i; break;
    }
    if (eqAt !== -1){
      const beforeEq = line.slice(0, eqAt).trim();   // ชื่อกล่องที่อยู่ก่อน =
      const afterEq  = line.slice(eqAt + 1).trim();  // ค่าที่อยู่หลัง =
      const firstTok = beforeEq.split(/\s+/)[0];      // คำแรกก่อน = (ดูว่าเป็นคำสั่งพิเศษไหม)

      // ใช้ = ตัวเดียวในเงื่อนไข ที่จริงต้องใช้ == (พลาดบ่อยตอนเรียน if/elif/while)
      if (firstTok === "if" || firstTok === "elif" || firstTok === "while"){
        return "ในเงื่อนไข if / elif / while ใช้ == (เท่ากับสองตัว) เพื่อ \"เปรียบเทียบ\" นะ ไม่ใช่ = ตัวเดียวที่แปลว่า \"กำหนดค่า\" เช่น if score == 50:";
      }

      // ขึ้นต้นด้วยคำสั่งพิเศษอื่น (for/def/return/else/...) = ไม่ใช่การตั้งชื่อตัวแปร
      // ข้ามการตรวจชื่อตัวแปรไป ปล่อยให้ตกไปจัดการแบบ SyntaxError ตามปกติ
      const stmtKw = ["else","for","def","return","class","with","try","except","finally",
                      "import","from","global","nonlocal","pass","break","continue",
                      "lambda","del","raise","yield","assert"];
      if (stmtKw.indexOf(firstTok) === -1){

        // 1) ลืมใส่ค่าหลัง =  เช่น  age =
        if (afterEq === ""){
          return "ตั้งชื่อกล่องไว้แล้ว แต่ยังไม่ได้ใส่ค่าให้นะ ลองใส่ค่าหลังเครื่องหมาย = เช่น age = 18";
        }
        // 2) ชื่อตัวแปรมีเว้นวรรค  เช่น  my age = 20
        if (/\s/.test(beforeEq)){
          return "ชื่อกล่อง (ตัวแปร) ห้ามมีเว้นวรรคนะ ลองเขียนติดกัน หรือใช้ _ คั่น เช่น my_age = 20";
        }
        // 3) ชื่อตัวแปรขึ้นต้นด้วยตัวเลข  เช่น  1age = 20
        if (/^[0-9]/.test(beforeEq)){
          return "ชื่อกล่อง (ตัวแปร) ห้ามขึ้นต้นด้วยตัวเลขนะ ลองขึ้นต้นด้วยตัวอักษร เช่น age1 = 20";
        }
        // 4) ชื่อตัวแปรมีเครื่องหมายที่ใช้ไม่ได้ เช่น ขีด -  (age-now = 20)
        if (beforeEq !== "" && /[^A-Za-z0-9_]/.test(beforeEq)){
          return "ชื่อกล่อง (ตัวแปร) ใช้ได้แค่ตัวอักษร ตัวเลข และ _ เท่านั้นนะ ลองเอาเครื่องหมายอื่นออก เช่น age_now = 20";
        }
        // 5) ใช้คำสงวน/คำพิเศษของ Python มาตั้งชื่อ เช่น True = 20 / print = 20 / list = 3
        const reserved = ["True","False","None","and","or","not","if","else","elif",
                          "for","while","def","return","import","class","try","except",
                          "print","input","list","dict","str","int","float","type","len"];
        if (reserved.includes(beforeEq)){
          return "คำนี้เป็นคำพิเศษของ Python ใช้ตั้งชื่อกล่องไม่ได้นะ ลองเปลี่ยนเป็นชื่ออื่น เช่น age = 20";
        }
      }
    }
  }

  // SyntaxError มีหลายกรณีย่อย แยกข้อความให้ตรงอาการ
  // เรียงจากเฉพาะเจาะจง -> กว้าง เพื่อจับอาการมือใหม่ให้ตรงก่อน fallback
  if (type === "SyntaxError"){

    // (1) อัญประกาศเปิด-ปิดไม่ตรงชนิด เช่น print('Rin") หรือ print("Rin')
    // จับ: มีทั้ง ' และ " อย่างละ 1 ตัวในบรรทัด = เปิดอย่างหนึ่ง ปิดอีกอย่าง
    const sQuote = (line.match(/'/g) || []).length;
    const dQuote = (line.match(/"/g) || []).length;
    if (sQuote === 1 && dQuote === 1){
      return "เครื่องหมายคำพูดเปิดกับปิดไม่เหมือนกัน ต้องใช้แบบเดียวกันทั้งคู่ เช่น \"ข้อความ\" หรือ 'ข้อความ'";
    }

    // (2) ลืมวงเล็บหลัง print เช่น print "Rin" (มี print เว้นวรรคแล้วตามด้วยข้อความ)
    if (/\bprint\s+['"]/.test(line)){
      return "อย่าลืมใส่วงเล็บ ( ) ครอบข้อความที่จะ print นะ เช่น print(\"ข้อความ\")";
    }

    // (3) มีคำ/ข้อความที่ไม่ได้ครอบด้วยอัญประกาศใน print เช่น print(Rin Smith)
    // จับ: ในวงเล็บมีตัวอักษรหลายคำคั่นด้วยช่องว่าง โดยไม่มีอัญประกาศเลย
    const inParen = /\bprint\s*\(([^)]*)\)/.exec(line);
    if (inParen){
      const inside = inParen[1];
      if (inside && !/['"]/.test(inside) && /[A-Za-z฀-๿]+\s+[A-Za-z฀-๿]+/.test(inside)){
        return "ถ้าจะให้แสดงเป็นข้อความ ต้องครอบด้วยเครื่องหมาย \" \" นะ เช่น print(\"Rin Smith\") แต่ถ้าตั้งใจเรียกตัวแปร ลองเช็คว่าสร้างตัวแปรไว้แล้วหรือยัง";
      }
    }

    // (4) ลืมใส่ + เชื่อมข้อความ (พบบ่อยใน Mission 3) เช่น print("ยินดีต้อนรับ " name)
    // จับ: สตริงอยู่ติดกับตัวแปร/สตริงอีกตัวโดยไม่มี + คั่น (รองรับทั้ง " และ ')
    if (!line.includes("+") && /["'][^"']*["']\s+[A-Za-z_"']/.test(line)){
      return "ลืมใส่ + เชื่อมข้อความไหม? ถ้าจะต่อข้อความกับตัวแปร ต้องมี + คั่นกลาง เช่น print(\"ยินดีต้อนรับ \" + name)";
    }

    // (5) f-string เปิดปีกกา { ไว้แต่ลืมปิด } เช่น print(f"คะแนน {score")
    if (lower.includes("f-string"))
      return "ใน f-string ที่เปิดปีกกา { ไว้ ต้องปิดด้วย } ให้ครบนะ เช่น print(f\"คะแนน {score}\")";

    // (6) ใช้ return นอกฟังก์ชัน — return ต้องอยู่ข้างใน def ที่ย่อหน้าเข้าไป
    if (lower.includes("'return' outside function"))
      return "คำสั่ง return ต้องอยู่ \"ข้างใน\" ฟังก์ชัน (ใต้ def ที่ย่อหน้าเข้าไป 4 ช่อง) นะ";

    // (7) ลูป for ขาดคำว่า in เช่น for i range(3):  (ลืม in)
    if (/^\s*for\b/.test(line) && !/\bin\b/.test(line))
      return "ลูป for ต้องมีคำว่า in นะ เช่น for i in range(3):";

    // (8) สร้างฟังก์ชันด้วย def แต่ลืมวงเล็บ () หลังชื่อ เช่น def greet:
    if (/^\s*def\b/.test(line) && !line.includes("("))
      return "การสร้างฟังก์ชันด้วย def ต้องมีวงเล็บ ( ) หลังชื่อนะ เช่น def greet():";

    // (9) เปิดวงเล็บ/เครื่องหมายไว้แต่ปิดไม่ครบ (Skulpt มักรายงานเป็น EOF)
    if (lower.includes("eof") || lower.includes("multi-line"))
      return "ดูเหมือนเปิดวงเล็บหรือเครื่องหมายไว้แต่ยังปิดไม่ครบนะ ลองนับ ( ) หรือ \" \" ให้เท่ากัน";

    // (10) fallback กลาง ๆ — ไม่ชี้นำเรื่อง : อีกต่อไป (กันแปลผิดในบทที่ยังไม่สอน if/for/def)
    return "รูปแบบบรรทัดนี้ยังไม่ถูกหลักภาษา Python ลองเทียบกับตัวอย่างด้านบนทีละตัวอักษร โดยเฉพาะวงเล็บ ( ) และเครื่องหมายคำพูด \" \" ว่าครบและตรงกันไหม";
  }

  // ตาราง error ที่เหลือ -> ข้อความไทย (คงเดิมทั้งหมด ไม่แตะ)
  const table = {
    "IndentationError":"บรรทัดนี้เว้นช่องหน้าไม่ตรงกับเพื่อน ลองจัดย่อหน้าให้ตรง (4 ช่อง)",
    "TabError":"บรรทัดนี้ปน Tab กับ Space ลองลบย่อหน้าแล้วเคาะ Space 4 ครั้งใหม่",
    "NameError":"มีชื่อตัวแปรหรือฟังก์ชันที่ยังไม่ถูกสร้าง หรือสะกดผิด ลองเช็คตัวสะกดอีกที",
    "TypeError":"เอาข้อมูลคนละชนิดมาทำด้วยกันไม่ได้ (เช่น ตัวเลข + ข้อความ) ต้องแปลงชนิดก่อนด้วย str() หรือ int()",
    "ValueError":"ค่าที่ใส่ไม่ตรงกับที่ต้องการ (เช่น เอาตัวอักษรไปแปลงเป็นเลขด้วย int())",
    "IndexError":"เรียกตำแหน่งใน list เกินที่มี จำไว้ว่าเริ่มนับจาก 0 นะ",
    "KeyError":"เรียก key ที่ไม่มีใน dictionary ลองเช็คชื่อ key",
    "AttributeError":"เรียกคำสั่งที่ชนิดข้อมูลนี้ไม่มี ลองเช็คว่าใช้ถูกชนิดไหม",
    "ZeroDivisionError":"หารด้วย 0 ไม่ได้นะ ลองเช็คตัวหาร",
    "ImportError":"โมดูลนี้ยังใช้ไม่ได้ในบทเรียนนี้ ใช้คำสั่งพื้นฐานก่อนนะ",
    "ModuleNotFoundError":"ไม่พบโมดูลที่เรียก บทเรียนนี้ยังไม่ต้อง import",
    "RecursionError":"ฟังก์ชันเรียกตัวเองวนไม่หยุด ลองใส่เงื่อนไขให้หยุดได้",
    "TimeLimitError":"โค้ดทำงานนานเกินไป อาจเป็นลูปที่ไม่มีวันหยุด ลองเช็คเงื่อนไข while",
    "OverflowError":"ตัวเลขใหญ่เกินจะคำนวณไหว ลองลดขนาดลง",
    "AssertionError":"เงื่อนไขใน assert เป็นเท็จ ลองเช็คค่าที่ได้",
    "UnboundLocalError":"ใช้ตัวแปรในฟังก์ชันก่อนกำหนดค่า ลองกำหนดค่าก่อนใช้",
    "KeyboardInterrupt":"การทำงานถูกหยุดกลางคัน ลองรันใหม่อีกครั้ง"
  };

  if (table[type]) return table[type];

  // เผื่อ error แปลก ๆ ที่ไม่อยู่ในตาราง -> ข้อความปลอบใจกลาง ๆ พร้อมชี้ทางต่อ
  return "มีบางอย่างยังไม่ถูก ลองอ่านโค้ดอีกรอบทีละบรรทัดช้า ๆ นะ ถ้าติดจริง ๆ กดปุ่ม \"ขอคำใบ้\" ได้เลย";
}
